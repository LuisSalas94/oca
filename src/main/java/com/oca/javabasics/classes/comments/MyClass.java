package com.oca.javabasics.classes.comments;

class MyClass {
    /**
     * Prints a greeting message with the specified name.
     *
     * @param name the name to include in the message
     * @return a greeting message in the format "Hello: [name]"
     */
    public static String printName(String name) {
        return "Hello: " + name;
    }

    public static void main(String[] args) {
        System.out.println("Salas");
    }
}





